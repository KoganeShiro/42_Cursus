#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	size_t	cpt;

	i = 0;
	cpt = 0;
	while (s[i++])
		cpt++;
	write(fd, s, cpt);
	write(fd, "\n", 1);
}
