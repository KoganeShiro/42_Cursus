int	ft_str_is_printable(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if (!(str[c] >= ' ' && str[c] <= '~'))
		{
			return (0);
		}
		c++;
	}
	return (1);
}
