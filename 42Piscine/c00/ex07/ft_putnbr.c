#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	unite;
	int	base_10;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb > 9)
	{
		unite = nb % 10;
		base_10 = nb / 10;
		ft_putnbr(base_10 + '0');
		ft_putchar(unite + '0');
	}
	else
		ft_putchar(nb + '0');
}
